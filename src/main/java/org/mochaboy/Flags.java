package org.mochaboy;

public class Flags {
    private String Z;
    private String N;
    private String H;
    private String C;

    public Flags(){}

    public String getZ() {
        return Z;
    }

    public void setZ(String z) {
        Z = z;
    }

    public String getN() {
        return N;
    }

    public void setN(String n) {
        N = n;
    }

    public String getH() {
        return H;
    }

    public void setH(String h) {
        H = h;
    }

    public String getC() {
        return C;
    }

    public void setC(String c) {
        C = c;
    }
}
